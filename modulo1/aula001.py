# ESTRUTURAS DE DADOS - VARIÁVEIS

# inteiro
a = 4
print(type(a))

# string
frase = "Hello, World!"
print(type(frase))

# ponto flutuante
b = 2.500
print(type(b))

# lógico
verdade = True
print(type(verdade))

# complexo
s = 1 + 2j
print(type(s))


